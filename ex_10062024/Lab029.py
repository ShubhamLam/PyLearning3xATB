# Arithmetic Operators
# +,-,*,/, %

a = 180
b = 90
print(a + b)
print(a - b)
print(a * b)
print(a / b)  # Float - Why Python is smart - div

print(a % b)  #modulus operator , output will be reminder
print(10 ** 2)    # 10 to the power 2 = 100
r = pow(10,2)
print(r)

#Modulus - Operator -> Reminder
# 90 | 180 | 2
#    | 180 |
#    |   0 |

print(87 % 10)   #modulus operator, output -> Reminder
print(87 // 10)   	#Floor division , output -> Quotient
